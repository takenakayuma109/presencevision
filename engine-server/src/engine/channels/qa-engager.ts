/**
 * Q&A Engager -- Q&Aサイトでの質問検索・回答投稿
 *
 * Reddit, Quora, Yahoo!知恵袋, 知乎(Zhihu)等で
 * 関連キーワードの質問を検索し、ブランドに自然に言及する
 * 有益な回答を投稿する。
 */

import type { Page } from "playwright";
import { getBrowserPool } from "../browser-pool.js";
import {
  startActivity,
  completeActivity,
  failActivity,
  addArtifact,
} from "../activity-logger.js";
import type { ChannelConfig, ChannelType } from "./channel-registry.js";
import type { PostResult } from "./social-poster.js";

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export interface QAEngageParams {
  projectId: string;
  taskId: string;
  channel: ChannelConfig;
  keyword: string;
  brandName: string;
  answerContent: string; // pre-generated by Ollama
}

// ---------------------------------------------------------------------------
// Screenshot helper
// ---------------------------------------------------------------------------

async function captureScreenshot(
  page: Page,
  activityId: string,
  title: string,
): Promise<void> {
  try {
    const screenshot = await page.screenshot({ fullPage: false, type: "png" });
    addArtifact(activityId, {
      type: "screenshot",
      title,
      content: screenshot.toString("base64"),
      mimeType: "image/png",
    });
  } catch (err) {
    console.warn(`[QAEngager] Screenshot failed: ${err}`);
  }
}

// ---------------------------------------------------------------------------
// Login helper
// ---------------------------------------------------------------------------

async function loginIfNeeded(
  page: Page,
  channel: ChannelConfig,
  loginUrl: string,
  usernameSelector: string,
  passwordSelector: string,
  submitSelector: string,
): Promise<void> {
  if (!channel.credentials?.username || !channel.credentials?.password) {
    console.log(`[QAEngager] No credentials for ${channel.name}, skipping login`);
    return;
  }

  await page.goto(loginUrl, { waitUntil: "domcontentloaded", timeout: 30_000 });
  await page.waitForTimeout(2000);

  const usernameField = page.locator(usernameSelector).first();
  if (!(await usernameField.isVisible({ timeout: 3000 }).catch(() => false))) {
    console.log(`[QAEngager] Already logged in to ${channel.name}`);
    return;
  }

  await usernameField.fill(channel.credentials.username);
  await page.waitForTimeout(500);

  const passwordField = page.locator(passwordSelector).first();
  await passwordField.fill(channel.credentials.password);
  await page.waitForTimeout(500);

  await page.locator(submitSelector).first().click();
  await page.waitForTimeout(5000);
}

