// ---------------------------------------------------------------------------
// Router
// ---------------------------------------------------------------------------

const QA_HANDLERS: Partial<
  Record<
    ChannelType,
    (page: Page, params: QAEngageParams, activityId: string) => Promise<PostResult>
  >
> = {
  reddit: engageOnReddit,
  quora: engageOnQuora,
  yahoo_chiebukuro: engageOnYahooChiebukuro,
  zhihu: engageOnZhihu,
};

// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------

export async function engageOnQA(params: QAEngageParams): Promise<PostResult> {
  const activity = startActivity({
    projectId: params.projectId,
    taskId: params.taskId,
    type: "social_posting",
    country: params.channel.regions[0] ?? "GLOBAL",
    language: params.channel.languages[0] ?? "en",
    method: "QAEngagement",
    description: `Q&A回答: ${params.channel.name} — キーワード "${params.keyword}"`,
  });

  const pool = getBrowserPool();

  try {
    const handler = QA_HANDLERS[params.channel.type];
    if (!handler) {
      const result: PostResult = {
        success: false,
        error: `Unsupported Q&A platform: ${params.channel.type}`,
        channel: params.channel.type,
        action: "answer_question",
      };
      failActivity(activity.id, result.error!);
      return result;
    }

    const result = await pool.withPage(
      async (page) => handler(page, params, activity.id),
      { country: params.channel.regions[0] },
    );

    addArtifact(activity.id, {
      type: "json",
      title: `Q&A回答結果: ${params.channel.name}`,
      content: JSON.stringify(result, null, 2),
    });

    if (result.success) {
      completeActivity(activity.id, {
        metrics: { answered: 1 },
        details: {
          postUrl: result.postUrl,
          channel: params.channel.type,
          keyword: params.keyword,
        },
      });
    } else {
      failActivity(activity.id, result.error ?? "No suitable question found");
    }

    return result;
  } catch (error) {
    const errorMsg = error instanceof Error ? error.message : String(error);
    failActivity(activity.id, errorMsg);
    return {
      success: false,
      error: errorMsg,
      channel: params.channel.type,
      action: "answer_question",
    };
  }
}
